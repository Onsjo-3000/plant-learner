export function getAllPlantsInCategory(categoryId) {
  const category = categories.find(c => c.id === categoryId);
  if (!category) return [];
  return category.levels.flatMap(level => level.plants);
}

export function getPlantsForLevel(categoryId, levelId) {
  const category = categories.find(c => c.id === categoryId);
  if (!category) return [];
  const level = category.levels.find(l => l.id === levelId);
  return level ? level.plants : [];
}
